  static String translate(BuildContext context, String key) {
    try {
      final langCode = Provider.of<LanguageProvider>(context).currentLanguage;
      if (_translations.containsKey(langCode)) {
        if (_translations[langCode]!.containsKey(key)) {
          return _translations[langCode]![key]!;
        }
      }
      if (_translations['en']!.containsKey(key)) {
        return _translations['en']![key]!;
      }
    } catch (e) {
      // In case provider is not accessible
    }
    return key;
  }
